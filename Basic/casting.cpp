#include<iostream>
using namespace std;
//typcasting is a process of converting one data type to another data type.
//In C++, there are two types of typcasting:
//1. Implicit typcasting
//2. Explicit typcasting
int main(){
    //Implicit typcasting
    int a = 'a';
    cout<<a<<endl;
    //Explicit typcasting
    char ch = 97;
    cout<<ch<<endl;
    return 0;
}